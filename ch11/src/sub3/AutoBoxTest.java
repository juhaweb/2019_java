package sub3;

public class AutoBoxTest {

	
	public static void main(String[] args) {
		
		// Auto Boxing
		Boolean box1 = false;
		Integer	box2 = 2;
		Double	box3 = 3.14;
		
		System.out.println(box1);
		System.out.println(box2);
		System.out.println(box3);
		
		
		// Auto Unboxing
		Boolean ubx1 = box1;
		Integer	ubx2 = box2;
		Double	ubx3 = box3;
		
		System.out.println(ubx1);
		System.out.println(ubx2);
		System.out.println(ubx3);
		
	}
	
	
	
}
 