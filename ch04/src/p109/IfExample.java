package p109;

public class IfExample {

	public static void main(String[] args) {
		
		int score = 93;
		
		if(score >= 90) {
			System.out.println("점수가 90보다 큽니다. ");
			System.out.println(" A 등급입니다. ");
		}
		
		if(score < 90) {
			System.out.println("점수가 90보다 큽니다. ");
			System.out.println(" B 등급입니다. ");			
		}
		
		
	}
	
}
